insert into sys_role_permissions (role_id, permission_id)
select distinct rp.role_id, approval_permission.id
from sys_role_permissions rp
join sys_permissions existing_permission on existing_permission.id = rp.permission_id
join sys_permissions approval_permission on approval_permission.permission_code in (
    'approval.read', 'approval.submit', 'approval.approve', 'approval.config.manage'
)
where existing_permission.permission_code = 'system.role.manage'
  and not exists (
      select 1
      from sys_role_permissions existing_role_permission
      where existing_role_permission.role_id = rp.role_id
        and existing_role_permission.permission_id = approval_permission.id
  );
